package interFan;

public interface Fan {
   //Fan 인터페이스를 구현한 클래스 3개를 만든다
	
	void on();
	void off();
	void turbo();
	
	
   
//   on(), off(), turbo() 를 구현한다
//   1. CeilingFan 클래스 
//   on() 천장선풍키를 켭니다
//   off() 천장선풍기를 끕니다
//   turbo() 
   
//   2. TableFan 클래스
//   on() 탁자선풍키를 켭니다
//   off() 탁자선풍기를 끕니다
//   turbo() 탁자 선풍기를 터보 모드로 전환합니다
   
//   3. TowerFan 클래스
//   on() 타워 선풍기를 켭니다
//   off() 타워 선풍기를 끕니다
//   turbo() 타워 선풍기를 터보 모드로 전환합니다
   

}